public class Places {

    //Objekte mit den 4 Parametern Name, X-Koordinate, Y-Koordinate, Typ

    private String name;
    private double x;
    private double y;
    private String type;

    public Places() {

    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }







}
